/// Returns the closest value for `x` that lies between `min` and `max` inclusively. The result is
/// undefined if `min > max`.
pub fn clamp<T: Ord>(x: T, min: T, max: T) -> T {
    if x < min {
        min
    } else if x > max {
        max
    } else {
        x
    }
}
